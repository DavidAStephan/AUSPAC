"""Phase R IRF benchmark comparison vs FR-BDF wp736.

Produces side-by-side quantitative comparison for all 7 FR-BDF shocks,
addressing audit actions #49-#52 and the Phase R refit definition of done.

Reads dynare/saved_irfs_{var,hybrid,mce}.mat (post Phase R Dynare runs at
irf=200) and outputs phase_r_benchmark_table.md with FR-BDF benchmarks
alongside AUSPAC values.

Usage (after Phase R Dynare runs):
    python3 dynare/regen/regen_phase_r_benchmarks.py
"""

from pathlib import Path
import numpy as np
from scipy.io import loadmat
try:
    import h5py
    HAVE_H5PY = True
except ImportError:
    HAVE_H5PY = False

HERE = Path(__file__).resolve().parent
DYNARE = HERE.parent  # dynare/ workspace

OUT = DYNARE / "phase_r_benchmark_table.md"

# FR-BDF wp736 §5.2 benchmarks (reading from paper):
# Each row: shock, variable, FR-BDF peak magnitude, FR-BDF peak quarter
FR_BDF_BENCHMARKS = [
    # eps_i: +100bp annualized monetary shock (§5.2.1)
    ("eps_i",   "ln_Q",         "Real GDP",                   -0.15,  12),
    ("eps_i",   "yhat_au",      "Output gap",                 -0.15,  12),
    ("eps_i",   "piQ",          "VA price inflation (qpp)",   -0.10,  12),
    # eps_tp: +100bp annualized term-premium shock (§5.2.2)
    ("eps_tp",  "ln_Q",         "Real GDP",                   -0.05,  10),
    ("eps_tp",  "ln_IB",        "Business inv level",         -0.45,  10),
    ("eps_tp",  "ln_IH",        "Household inv level",        -0.30,  10),
    # eps_q_us / eps_q_ea: foreign demand +1% shock (§5.2.3)
    ("eps_q_us","ln_Q",         "Real GDP",                   +0.14,   4),
    # eps_g: +1% of GDP gov spending shock (§5.2.4)
    ("eps_g",   "ln_Q",         "Real GDP (impact multiplier)",  +1.20,  1),
    ("eps_g",   "yhat_au",      "Output gap (impact)",        +1.20,   1),
    # eps_pcom: +10% commodity (oil) price shock (§5.2.5)
    ("eps_pcom","ln_Q",         "Real GDP",                   -0.20,  12),
    ("eps_pcom","piQ",          "VA price inflation (qpp)",   -0.06,  12),
    # eps_pQ: +1% cost-push shock (§5.2.6)
    ("eps_pQ",  "ln_Q",         "Real GDP",                   -0.45,   8),
    # eps_tfp_LR: permanent +1% labor efficiency shock (§5.2.7)
    ("eps_tfp_LR", "ln_Q",      "Real GDP (4-year, % from BGP)", +0.60, 16),
]


class H5IRFs:
    """Wrapper for HDF5-stored IRFs from MATLAB v7.3 .mat files."""
    def __init__(self, path):
        self.f = h5py.File(path, "r")
        # First non-meta variable in the file
        self.key = next(k for k in self.f.keys() if not k.startswith("#"))
        self.group = self.f[self.key]

    def get(self, fld):
        if fld not in self.group:
            return None
        ds = self.group[fld]
        # MATLAB stores as 1xN; transpose if needed
        return np.asarray(ds).flatten()


def load_irfs(variant):
    p = DYNARE / f"saved_irfs_{variant}.mat"
    if not p.exists():
        return None
    try:
        d = loadmat(p, squeeze_me=True, struct_as_record=False)
        key = [k for k in d if not k.startswith("__")][0]
        return d[key]
    except NotImplementedError:
        # v7.3 HDF5 file — use h5py
        if not HAVE_H5PY:
            print(f"WARNING: {p.name} is v7.3 HDF5 — install h5py to read")
            return None
        return H5IRFs(p)


def get_irf(irfs, var, shock):
    fld = f"{var}_{shock}"
    if isinstance(irfs, H5IRFs):
        return irfs.get(fld)
    if not hasattr(irfs, fld):
        return None
    return np.asarray(getattr(irfs, fld))


def find_peak(arr, target_q):
    """Return (peak_value, peak_quarter) — peak is maximum |IRF| in window."""
    if arr is None or len(arr) == 0:
        return None, None
    n = len(arr)
    # Look in [target_q-4, target_q+4] window
    lo = max(0, target_q - 4)
    hi = min(n, target_q + 4)
    window = arr[lo:hi]
    idx = np.argmax(np.abs(window))
    return float(window[idx]), int(lo + idx + 1)  # 1-indexed quarter


def main():
    print("Loading saved_irfs_*.mat files...")
    irfs = {
        "var":    load_irfs("var"),
        "hybrid": load_irfs("hybrid"),
        "mce":    load_irfs("mce"),
    }

    if all(v is None for v in irfs.values()):
        print("ERROR: no saved_irfs_*.mat files found in dynare/.")
        print("       Run dynare au_pac_var, au_pac, au_pac_mce first.")
        return

    rows = ["# Phase R IRF benchmark comparison vs FR-BDF wp736",
            "",
            "Generated by `regen_phase_r_benchmarks.py` after Phase R refit.",
            "",
            "Format: peak |IRF| value at peak quarter (within ±4 of FR-BDF peak).",
            "",
            "| Shock | Variable | FR-BDF (peak @ Q) | AUSPAC VAR | AUSPAC Hybrid | AUSPAC MCE |",
            "|---|---|---|---|---|---|"]

    for shock, var, label, fr_peak, fr_q in FR_BDF_BENCHMARKS:
        cells = [f"`{shock}`", label, f"{fr_peak:+.2f} @ Q{fr_q}"]
        for variant in ["var", "hybrid", "mce"]:
            arr = get_irf(irfs[variant], var, shock) if irfs[variant] else None
            pk, pq = find_peak(arr, fr_q)
            if pk is None:
                cells.append("(missing)")
            else:
                cells.append(f"{pk:+.3f} @ Q{pq}")
        rows.append("| " + " | ".join(cells) + " |")

    rows.append("")
    rows.append("## Notes")
    rows.append("")
    rows.append("- FR-BDF benchmarks per wp736 §5.2 figures and text. Values may be")
    rows.append("  approximate (read from charts) or text-stated (e.g. eps_g multiplier).")
    rows.append("- AUSPAC values are scaled to match FR-BDF shock magnitudes (e.g. eps_i")
    rows.append("  shock of 0.25qpp ≈ 100bp annualized; eps_tfp_LR of +0.01 = +1% level).")
    rows.append("- Per Phase R refit: target is `< -0.20` for ln_Q under eps_i (closer to")
    rows.append("  FR-BDF -0.15 than the pre-refit -0.27 at Q40).")
    rows.append("- Phase Q forward UIP property: Hybrid > VAR for output gap (ratio ~1.18).")
    rows.append("  Phase L forward-guidance puzzle: linear ratio ~10 at N=12 (no puzzle).")

    OUT.write_text("\n".join(rows))
    print(f"Wrote {OUT.relative_to(DYNARE.parent)}")


if __name__ == "__main__":
    main()
